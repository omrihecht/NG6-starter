class CalculatorService {

    add(a,b){
      console.log('add');
      return a + b;
    }
    subtract(a,b){
      console.log('subtract');
      return a - b;
    }
    multiply(a,b){
      console.log('multiply');
      return a * b;
    }
    devide(a,b){
      console.log('devide');
      return a / b;
    }


}

export default CalculatorService;
